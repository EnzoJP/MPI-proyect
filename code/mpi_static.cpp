static const int MAX_VAL = 1000000000;
static const int MIN_VAL = 5000000;

// Global pointer for the local chunk of the matrix generated by each process.
// Each MPI rank will allocate `matrix` with space for its assigned rows
// (localRows * R) and fill it with deterministic values using
// `generateValue(seed, global_index)` so that the whole logical matrix
// is reproducible given the same `seed`.
static int* matrix = nullptr;


inline int generateValue(unsigned int seed, long long index)
{
    // Fast deterministic hash to generate value at specific index
    unsigned int x = seed ^ (index >> 32);
    x ^= x << 13;
    x ^= x >> 17;
    x ^= x << 5;
    x ^= index & 0xFFFFFFFF;
    x ^= x << 13;
    x ^= x >> 17;
    x ^= x << 5;
    return MIN_VAL + (x % (MAX_VAL - MIN_VAL + 1));
}

// Build a local chunk of the global matrix: rows [startRow, startRow+rowCount)
// Implementation details:
// - `startRow` is the index of the first row in the global matrix that
//   this chunk corresponds to, calculated by rank.
// - `rowCount` is how many rows this process must generate.
// - `R` is the number of columns per row.
// - The function computes a `baseIndex = startRow * R` and then fills
//   the local linear buffer `matrix[0..rowCount*R-1]` with values
//   generated by `generateValue(seed, baseIndex + k)` for k in [0,total).
// - This avoids storing the whole global matrix on a single process and
//   allows each rank to independently reproduce the same values.
void buildMatrixChunk(int startRow, int rowCount, int R, unsigned int seed) {
    if (rowCount <= 0) return;
    long long baseIndex = (long long)startRow * R;
    long long total = (long long)rowCount * R;
    for (long long k = 0; k < total; ++k) {
        // map local offset k to the corresponding global linear index
        // and generate the deterministic value for that position
        matrix[k] = generateValue(seed, baseIndex + k);
    }
}



bool isPrime(int n)
{
    if (n < 2) return false;
    if (n == 2) return true;
    if (n % 2 == 0) return false;

    int raiz = static_cast<int>(std::sqrt(static_cast<double>(n)));
    for (int i = 3; i <= raiz; i += 2) {
        if (n % i == 0) return false;
    }
    return true;
}

